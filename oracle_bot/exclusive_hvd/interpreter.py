# Возрастные ориентиры (~12 лет на цифру кода)
PERIOD_AGE_YEARS = 12


def period_age_range(index: int) -> str:
    start = index * PERIOD_AGE_YEARS
    end = start + PERIOD_AGE_YEARS
    if index == 0:
        return f"0–{end} лет"
    return f"{start}–{end} лет"


def life_code_digits(negative_code: int) -> list[int]:
    return [int(c) for c in str(abs(negative_code)) if c.isdigit()]
