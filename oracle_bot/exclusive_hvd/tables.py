def days_in_month(month: int, year: int) -> int:
    if month == 2 and isleap(year):
        return 29
    return _MONTH_DAYS[month - 1]


def month_markers(month: int, year: int) -> tuple[int, int, int]:
    if month == 1 and isleap(year):
        return _JAN_LEAP_MARKERS
    return _MONTH_MARKERS[month]


def reduce_marker(total: int, cycle: int) -> int:
    while total > cycle:
        total -= cycle
    return total
