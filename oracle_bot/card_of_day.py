def card_for_user(user_id: int, day: str | None = None) -> tuple[str, str]:
    day = day or date.today().isoformat()
    seed = hashlib.sha256(f"{user_id}:{day}".encode()).hexdigest()
    idx = int(seed[:8], 16) % len(MAJOR_ARCANA)
    card = MAJOR_ARCANA[idx]
    hint = _CARD_HINTS.get(card, "Обрати внимание на знаки сегодня.")
    return card, hint


def format_card_message(user_id: int) -> str:
    card, hint = card_for_user(user_id)
    return (
        f"<b>Карта дня</b> · {card}\n\n"
        f"{hint}\n\n"
        "<i>Хочешь расклад под свой вопрос — выбери Таро в меню.</i>"
    )
