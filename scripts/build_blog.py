def _faq_schema(faq: list[tuple[str, str]]) -> str:
    import json
    items = [{
        "@type": "Question", "name": q,
        "acceptedAnswer": {"@type": "Answer", "text": a},
    } for q, a in faq]
    return json.dumps({"@context": "https://schema.org", "@type": "FAQPage",
                       "mainEntity": items}, ensure_ascii=False)


def _article_schema(a: dict) -> str:
    import json
    return json.dumps({
        "@context": "https://schema.org", "@type": "Article",
        "headline": a["title"], "description": a["desc"],
        "inLanguage": "ru", "author": {"@type": "Organization", "name": "Оракул (MOracul_bot)"},
        "mainEntityOfPage": f"{BASE}/blog/{a['slug']}",
    }, ensure_ascii=False)


